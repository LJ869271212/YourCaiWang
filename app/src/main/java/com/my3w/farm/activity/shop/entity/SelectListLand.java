package com.my3w.farm.activity.shop.entity;

public class SelectListLand {

	private String id;
	private String digital;
	private String no;
	private String sell_status;
	private String acreage;
	private int backgroud;
	private float price;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDigital() {
		return digital;
	}

	public void setDigital(String digital) {
		this.digital = digital;
	}

	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public String getSell_status() {
		return sell_status;
	}

	public void setSell_status(String sell_status) {
		this.sell_status = sell_status;
	}

	public String getAcreage() {
		return acreage;
	}

	public void setAcreage(String acreage) {
		this.acreage = acreage;
	}

	public int getBackgroud() {
		return backgroud;
	}

	public void setBackgroud(int backgroud) {
		this.backgroud = backgroud;
	}

	public float getPrice() {
		return price;
	}

	public void setPrice(float price) {
		this.price = price;
	}

}
